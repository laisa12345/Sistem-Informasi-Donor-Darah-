    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        // TODO add your handling code here:
        String nama = txt_Nama.getText();
        String golonganDarah = txt_GolonganDarah.getText();
        int tanggalLahir = Integer.parseInt(txt_TanggalLahir.getText());
 
        Pendonor pd = new Pendonor();
        pd.setNama(nama);                        // ✅ pakai setter
        pd.setGolonganDarah(golonganDarah);     // ✅ pakai setter dari superclass
        pd.setTanggalLahir(tanggalLahir);       // ✅ pakai setter

// 4. Proses pengecekan
        txt_Output.setText("");
        txt_Output.append("\tHasil Pengecekan\n");
        txt_Output.append("========================\n");

        int tglLahir = pd.getTanggalLahir();  // ✅ pakai getter
        String formattedTgl = String.format("%02d-%02d-%04d",
                tglLahir % 100,
                (tglLahir % 10000) / 100,
                tglLahir / 10000);

        txt_Output.append("Nama: " + pd.getNama() + "\n"); // ✅ pakai getter
        txt_Output.append("Umur: " + hitungUmur(tglLahir) + " tahun\n");
        txt_Output.append("Gol. Darah: " + pd.getGolonganDarah() + "\n");
        txt_Output.append("Tgl Lahir: " + formattedTgl + "\n");

// 5. Cek kelayakan berdasarkan umur saja
        int umur = hitungUmur(tglLahir);
        boolean layak = umur >= 17;

// 6. Tampilkan hasil
        if (layak) {
            txt_Output.append("Status: BOLEH DONOR\n");
        } else {
            txt_Output.append("Status: TIDAK BOLEH DONOR\n");
        }

        String sisaHari = txt_SisaHari.getText();
        txt_Output.append("\nInfo Donor:\n" + sisaHari + "\n");
    }//GEN-LAST:event_btnCetakActionPerformed

    private void btnSisaHariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSisaHariActionPerformed
        // TODO add your handling code here:
        // Parsing input tanggal donor dari field
        String input = txt_TanggalDonorBaru.getText().trim();

        if (input.length() != 8) {
            txt_SisaHari.setText("Format tanggal salah, harus yyyymmdd");
            return;
        }

        int yyyy = Integer.parseInt(input.substring(0, 4));
        int mm = Integer.parseInt(input.substring(4, 6));
        int dd = Integer.parseInt(input.substring(6, 8));

        // Validasi bulan
        if (mm < 1 || mm > 12) {
            txt_SisaHari.setText("Bulan harus antara 01 sampai 12");
            return;
        }

        // Validasi hari manual (tanpa try-catch)
        int[] hariPerBulan = {31, (isKabisat(yyyy) ? 29 : 28), 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
        if (dd < 1 || dd > hariPerBulan[mm - 1]) {
            txt_SisaHari.setText("Hari tidak valid untuk bulan tersebut");
            return;
        }

        int tanggalDonor = Integer.parseInt(input);
        HistoryPendonor hp = new HistoryPendonor();
        String hasil = hp.getNextDonorInfo(tanggalDonor);

        txt_SisaHari.setText(hasil);
    }

    private boolean isKabisat(int tahun) {
        return (tahun % 4 == 0 && tahun % 100 != 0) || (tahun % 400 == 0);
    }//GEN-LAST:event_btnSisaHariActionPerformed

