    private int hitungUmur(int tglLahir) {
        LocalDate sekarang = LocalDate.now();
        LocalDate tglLahirObj = LocalDate.of(
                tglLahir / 10000,
                (tglLahir % 10000) / 100,
                tglLahir % 100
        );
        return Period.between(tglLahirObj, sekarang).getYears();
    }
    
