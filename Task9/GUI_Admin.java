    private void btn_CetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_CetakActionPerformed
        // TODO add your handling code here:
         try {
            // Ambil data dari form
            String nama = txt_Nama.getText().trim();
            String golonganDarah = txt_GolonganDarah.getText().trim().toUpperCase();
            String strTanggalLahir = txt_TanggalLahir.getText().trim();

            // Validasi 1: Cek field tidak kosong
            if (nama.isEmpty() || golonganDarah.isEmpty() || strTanggalLahir.isEmpty()) {
                txt_Output.setText("ERROR: Semua field harus diisi!");
                return;
            }

            // Validasi 2: Format golongan darah (A/B/AB/O diikuti + atau -)
            boolean validGolonganDarah = golonganDarah.matches("^(A|B|AB|O)[+-]$");
            if (!validGolonganDarah) {
                txt_Output.setText("ERROR: Format golongan darah salah!\n"
                        + "Contoh yang benar: A+, B-, AB+, O-");
                return;
            }

            // Validasi 3: Tanggal lahir harus 8 digit angka (YYYYMMDD)
            boolean validTanggal = strTanggalLahir.matches("^\\d{8}$");
            if (!validTanggal) {
                txt_Output.setText("ERROR: Tanggal lahir harus 8 digit angka\n"
                        + "Contoh: 20001231 untuk 31 Desember 2000");
                return;
            }

            // Konversi tanggal lahir
            int tanggalLahir = Integer.parseInt(strTanggalLahir);

            // Hitung umur pakai fungsi hitungUmur
            int umur = hitungUmur(tanggalLahir);

            // Buat objek Pendonor
            Pendonor pendonor = new Pendonor();
            pendonor.setNama(nama);
            pendonor.setGolonganDarah(golonganDarah);
            pendonor.setTanggalLahir(tanggalLahir);

            // Cek kelayakan donor, dan umur minimal 17 tahun
            boolean layakUmur = umur >= 17;
            boolean layakPendonor = pendonor.cekKelayakanDonor() && layakUmur;

            // Format tanggal lahir untuk output
            String formattedTgl = String.format("%02d-%02d-%04d",
                    tanggalLahir % 100,
                    (tanggalLahir % 10000) / 100,
                    tanggalLahir / 10000);

            String output = "=== DATA PENDONOR ===\n"
                    + "Nama: " + pendonor.getNama() + "\n"
                    + "Gol. Darah: " + pendonor.getGolonganDarah() + "\n"
                    + "Tgl Lahir: " + formattedTgl + "\n"
                    + "Umur: " + umur + " tahun\n"
                    + "Status: " + (layakPendonor ? "LAYAK" : "TIDAK LAYAK") + "\n";

            if (!layakUmur) {
                output += "Peringatan: Pendonor belum cukup umur (minimal 17 tahun).\n";
                JOptionPane.showMessageDialog(this, "Pendonor belum cukup umur (minimal 17 tahun).", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

            txt_Output.setText(output);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Format tanggal lahir tidak valid. Gunakan format: yyyymmdd", "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_CetakActionPerformed

