    public int hitungUmur(int yyyymmdd) {
        try {
            String str = String.valueOf(yyyymmdd);
            DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyyMMdd");
            LocalDate tglLahir = LocalDate.parse(str, formatter);
            LocalDate today = LocalDate.now();
            Period periode = Period.between(tglLahir, today);
            return periode.getYears();
        } catch (Exception e) {
            return 0; // jika gagal parse, umur dianggap 0
        }
    }

